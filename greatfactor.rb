def greatest_common_factor(num1,num2)  
  num1, num2 = num2, num1%num2  until num2 == 0   
  num1  
end



puts(
  'greatest_common_factor(3, 9) == 3: ' +
  (greatest_common_factor(3, 9) == 3).to_s
)
